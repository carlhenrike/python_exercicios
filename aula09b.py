frase = 'Curso em Vídeo Python'
print(frase.count('o'))  # procura posição da letra
print(frase.upper().count('O'))  # uppr deixa todas letras maisculas
print(len(frase))  # conta quantas letras tem na frase
print(frase.strip())  # remove espaços laterais
print(frase.replace('Python', 'Android'))  # substitui a palavra
print('Curso' in frase)    # diz se existe a palavra na frase
print(frase.find('Vídeo'))    # procura a posiçã da palavra na frase


